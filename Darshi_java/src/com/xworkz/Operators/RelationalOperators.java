package com.xworkz.Operators;

public class RelationalOperators {
	public static void main (String args[]) {
		 int x=50;
		 int y=90;
		 boolean equalresult=(x==y);
		 boolean notequalresult=(x!=y);
		 boolean lessthanresult=(x<y);
		 boolean greaterthanresult=(x>y);
		 boolean lessthanequalresult=(x<=y);
		 System.out.println("equalresult="+equalresult);//false
		 System.out.println("notequalresult="+notequalresult);//true
		 System.out.println("lessthanresult="+lessthanresult);//true
		 System.out.println("greaterthanresult="+greaterthanresult);//false
		 System.out.println("lessthanequalresult="+lessthanequalresult);//true
		 System.out.println(x>=y);//false
		
		 
	}

}
