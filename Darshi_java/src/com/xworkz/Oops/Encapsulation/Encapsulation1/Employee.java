package com.xworkz.Oops.Encapsulation.Encapsulation1;

public class Employee {
	private int id;
	private String name;
	public void SetId(int id) {
		this.id =id;	
	}
    public void SetName(String name) {
    	this.name=name;
    	
    }
    public int GetId() {
    	return id;
    	
    }
    public String GetName() {
    	return name;
    	
    }
}
