package com.xworkz.Pattern;

public class Pattern6 {
	public static void main(String[] args) {
	    for (int i=1; i<=4;i++) {
	    	for (int j=4; j>=i;j--) {
	    		System.out.print("*");
	    	}
	    
	    	    	for (int r=1; r<=i;r++) {
	    	    		System.out.print(" ");
	    	    	}
	    	    	System.out.println();
	    	
	    }
	}

}
